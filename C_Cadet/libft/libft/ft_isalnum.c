#include "libft.h"

int	ft_isalnum(int a)
{
	if ('A' <= a && 'Z' >= a)
		return (1);
	if ('a' <= a && 'z' >= a)
		return (1);
	if ('0' <= a && '9' >= a)
		return (1);
	return (0);
}
