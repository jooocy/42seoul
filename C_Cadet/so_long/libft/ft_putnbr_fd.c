#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	tmp[100];

	i = 0;
	while (n / 10)
	{
		if (n < 0)
			tmp[i++] = ((n % 10) * (-1)) + '0';
		else
			tmp[i++] = (n % 10) + '0';
		n /= 10;
	}
	if (n < 0)
	{
		tmp[i++] = -n + '0';
		tmp[i] = '-';
	}
	else
		tmp[i] = n + '0';
	while (i >= 0)
		write(fd, &tmp[i--], 1);
}
