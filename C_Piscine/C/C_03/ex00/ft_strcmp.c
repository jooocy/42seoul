int	ft_strcmp(char *a1, char *a2)
{
	int	i;

	i = 0;
	while (a1[i])
	{
		if (a1[i] != a2[i])
			return (a1[i] - a2[i]);
		i++;
	}
	if (a1[i] == a2[i])
		return (0);
	return (a1[i] - a2[i]);
}
